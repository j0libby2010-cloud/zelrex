// ─── Output validation function ──────────────────────────────────

interface ValidationOptions {
  checkFinancial?: boolean;    // Check for unsourced financial claims
  checkContract?: boolean;     // Check for contract content without disclaimer
  checkGuarantee?: boolean;    // Check for guarantee language
  checkCompetitor?: boolean;   // Check for unsourced competitor claims
  checkOutreach?: boolean;     // Check for fabricated prospect details
  forceContractDisclaimer?: boolean;  // Always append contract disclaimer
  forceFinancialDisclaimer?: boolean; // Always append financial disclaimer
}

export function validateOutput(
  reply: string,
  options: ValidationOptions = {}
): string {
  let result = reply;
  const {
    checkFinancial = false, // Disabled — UI footer already says "Not financial or legal advice"
    checkContract = true,
    checkGuarantee = true,
    checkCompetitor = true,
    checkOutreach = false,
    forceContractDisclaimer = false,
    forceFinancialDisclaimer = false,
  } = options;

  // Force disclaimers if requested (for contracts, proposals)
  if (forceContractDisclaimer) {
    const hasDisclaimer = /AI-GENERATED|not reviewed by a lawyer|not legal advice|consult.*legal.*professional|AI-generated template/i.test(result);
    if (!hasDisclaimer) {
      result += CONTRACT_DISCLAIMER;
    }
  }

  if (forceFinancialDisclaimer) {
    const hasDisclaimer = /not financial advice|strategic guidance|verify.*independently|scenario estimate|not a financial advisor/i.test(result);
    if (!hasDisclaimer) {
      result += FINANCIAL_DISCLAIMER;
    }
  }

  // Check for business-relevant financial claims without uncertainty language
  if (checkFinancial) {
    const hasBusinessFinancialClaim = /\$\d{2,}(?:,\d{3})*(?:\.\d{2})?\s*(?:\/|per|a)\s*(?:month|year|hour|project|client|week)|(?:earn|make|charge|revenue|income|profit)\s+(?:of\s+)?\$\d+|(?:pricing|price|rate|fee)\s+(?:of|at|is)\s+\$\d+/i.test(result);
    const hasNaturalUncertainty = /I(?:'d| would) estimate|from what I've seen|in my experience|I'm estimating|my best estimate|based on what I know|typically report|tends? to be around|rough(?:ly| number)|ballpark|I'm not certain|I haven't verified|based on patterns|scenario|realistic range|could vary|depends (?:on|heavily)|not a (?:promise|guarantee)/i.test(result);
    const hasFinancialDisclaimer = /not financial advice|strategic guidance|verify.*independently|scenario estimate|not a financial advisor/i.test(result);

    if (hasBusinessFinancialClaim && !hasNaturalUncertainty && !hasFinancialDisclaimer) {
      result += FINANCIAL_DISCLAIMER;
    }
  }

  // Check for contract content without disclaimer
  if (checkContract) {
    const hasContractTemplate = /(?:^|\n)\s*(?:AGREEMENT|CONTRACT|SCOPE OF WORK|TERMS AND CONDITIONS|PAYMENT SCHEDULE|DELIVERABLES:)/mi.test(result);
    const hasContractDisclaimer = /AI-generated|AI-GENERATED|not reviewed by a lawyer|not legal advice|consult.*legal.*professional/i.test(result);
    if (hasContractTemplate && !hasContractDisclaimer) {
      result += CONTRACT_DISCLAIMER;
    }
  }

  // Check for guarantee language
  if (checkGuarantee) {
    const hasGuarantee = /(?:you will|you'll|I guarantee|guaranteed to)\s+(?:earn|make|get|receive|have)\b|100%\s+(?:certain|guaranteed|sure)|without a doubt you/i.test(result);
    if (hasGuarantee) {
      result += GUARANTEE_DISCLAIMER;
    }
  }

  // Check for unsourced competitor claims
  if (checkCompetitor) {
    const hasSpecificCompetitorClaim = /(?:companies? (?:like|such as)|competitors? (?:like|include|such as))\s+[A-Z][a-z]+(?:\s+[A-Z][a-z]+)?(?:\s+(?:charges?|offers?|earns?|makes?))/i.test(result);
    const hasNaturalSourceRef = /I found|according to|from (?:their|the) (?:website|site)|based on (?:my|a) search|I searched|I checked/i.test(result);
    if (hasSpecificCompetitorClaim && !hasNaturalSourceRef) {
      result += COMPETITOR_DISCLAIMER;
    }
  }

  return result;
}


// ─── Validate prospect claims in outreach emails ─────────────────

export function validateOutreachEmail(email: { subject: string; body: string }): { subject: string; body: string } {
  let body = email.body;
  let subject = email.subject;

  // 1. Remove suspiciously specific unsourced claims about the prospect
  const suspiciousClaims = /(?:your revenue|you(?:'re| are) (?:earning|making)|your (?:\d+ )?(?:employees?|team members?|staff)|you(?:'ve| have) been (?:in business|operating) for \d+)/i.test(body);
  if (suspiciousClaims) {
    body = body.replace(/(?:your revenue (?:of|is) \$[\d,]+)/gi, "your business");
    body = body.replace(/(?:your \d+ (?:employees?|team members?|staff))/gi, "your team");
    body = body.replace(/you(?:'ve| have) been (?:in business|operating) for \d+ years?/gi, "your business");
    body = body.replace(/you(?:'re| are) (?:earning|making) \$[\d,]+/gi, "your work");
  }

  // 2. Remove AI cold-email giveaway phrases
  const aiGiveaways = [
    { pattern: /\bI noticed (?:that )?you(?:'re| are) (?:looking|searching|in need|in the market)\b/gi, replacement: "I think" },
    { pattern: /\bI came across your (?:company|business|website)\b/gi, replacement: "I was looking at your business" },
    { pattern: /\bI hope this (?:email )?finds you well\b/gi, replacement: "" },
    { pattern: /\b(?:My name is|I'm) \w+ and I(?:'m| am) reaching out\b/gi, replacement: "Reaching out" },
  ];
  for (const g of aiGiveaways) {
    body = body.replace(g.pattern, g.replacement);
    subject = subject.replace(g.pattern, g.replacement);
  }

  // 3. Remove fake awards/recognition claims about the prospect
  body = body.replace(/(?:your|you(?:r)?) (?:recent |)(?:award|recognition|mention|feature) (?:in|on|by) [A-Z][a-z]+/gi, "your work");

  // 4. Clean up any double spaces or orphan punctuation from removals
  body = body.replace(/\s{2,}/g, ' ').replace(/\n\s*\n\s*\n+/g, '\n\n').trim();

  return { subject, body };
}


// ─── HAIKU FACT-CHECKER ──────────────────────────────────────────
// Runs after Opus generates a response. Uses the cheap Haiku model
// to scan for hallucinations, unsourced claims, and reliability issues.
// Cost: ~$0.001-0.003 per call. Adds ~1-2 seconds latency.

export async function factCheck(
  reply: string,
  userMessage: string
): Promise<string> {
  // Skip fact-checking for short/casual responses
  if (reply.length < 200) return reply;
  // Skip if response is clearly casual (greetings, simple acknowledgments)
  if (/^(hey|hi|hello|sure|got it|sounds good|nice|ok|understood)/i.test(reply.trim()) && reply.length < 400) return reply;

  try {
    const response = await anthropic.messages.create({
      model: process.env.ANTHROPIC_MODEL_HAIKU || "claude-haiku-4-5-20251001",
      max_tokens: 500,
      messages: [{
        role: "user",
        content: `You are a fact-checker for a business advisory AI called Zelrex. Check this AI response for reliability issues.

USER ASKED: "${userMessage.slice(0, 500)}"

AI RESPONDED:
${reply.slice(0, 3000)}

CHECK FOR THESE SPECIFIC ISSUES:
1. FABRICATED NUMBERS: Specific dollar amounts, percentages, or statistics stated as confident fact WITHOUT natural uncertainty language. Natural uncertainty looks like: "I'd estimate," "from what I've seen," "roughly," "typically," "in my experience," "based on what I know," "around," or referencing a specific source like "According to [X]." A number without any of these signals is a potential fabrication.
2. INVENTED NAMES: Specific company names, person names, or tool names presented as real. Watch for patterns like "companies like [Name]" or "tools such as [Name]" without the AI having actually searched for them.
3. FAKE SOURCES: Citations like "According to [Source]" or "A 2024 study by [Source]" where the source name could be made up.
4. GUARANTEE LANGUAGE: Phrases like "you will earn," "guaranteed," "definitely will," "is going to work."
5. FALSE MEMORY: Claims like "as we discussed" or "you mentioned" that could be fabricated recall of prior conversation.
6. OVERCONFIDENT MARKET CLAIMS: Statements about market size, growth rates, or industry trends presented as certain fact without uncertainty disclosure.
7. MADE-UP COMPETITORS: Listing specific competitors with pricing ("X charges $Y") without natural sourcing language.

RESPOND WITH EXACTLY ONE OF:
- "CLEAN" — if no issues found
- "FLAG: [one sentence describing the issue]" — if you found a specific problem

Be conservative. Only flag clear problems. If the response uses natural uncertainty language ("I'd estimate," "from what I've seen," etc.) or clearly cites a real source, mark it CLEAN. Do NOT look for bracketed tags like [SEARCHED] — those are deliberately not used anymore.`
      }],
    });

    const check = response.content
      .filter((b): b is Anthropic.TextBlock => b.type === "text")
      .map((b) => b.text)
      .join("")
      .trim();

    if (check.startsWith("FLAG:")) {
      const issue = check.replace("FLAG:", "").trim();
      // Append a subtle verification prompt for genuinely problematic content
      if (/fabricat|invent|made.?up|hallucin|fake|doesn.t exist|made up/i.test(issue)) {
        return reply + `\n\n*⚠️ I want to flag something in my response — ${issue.toLowerCase()} Worth double-checking before acting on it.*`;
      }
      if (/guarantee|promise|certain/i.test(issue)) {
        return reply + `\n\n*Worth noting: no outcome is guaranteed. Treat these as estimates based on patterns, not promises.*`;
      }
      if (/false.*memory|didn.t discuss|didn.t mention/i.test(issue)) {
        return reply + `\n\n*Note: I may have misremembered something from our earlier conversation. If anything above doesn't match what we actually discussed, let me know and I'll correct.*`;
      }
      if (/overconfident|certain.*fact|without uncertainty/i.test(issue)) {
        return reply + `\n\n*One caveat on the above: some of these numbers are my best estimate, not verified data. Worth validating independently if you're making a decision based on them.*`;
      }
      // For less severe flags, just log them
      console.log(`[ZELREX FACT-CHECK] Flag: ${issue}`);
    }

    return reply;
  } catch (error) {
    // If fact-check fails, just return the original reply — never block a response
    console.warn("[ZELREX FACT-CHECK] Error (non-blocking):", error);
    return reply;
  }
}


// ═══════════════════════════════════════════════════════════════
// CODE-LEVEL ENFORCEMENT — catches rule violations the prompt missed
// 
// The prompt ASKS Claude to follow rules.
// This code ENFORCES them after the fact.
// If Claude ignores a prompt rule, this catches it.
// ═══════════════════════════════════════════════════════════════

export interface EnforcementResult {
  reply: string;
  violations: string[];
  wasModified: boolean;
  shouldRetry: boolean;  // True = violation is so bad we should re-call Claude
}

export function enforceRules(reply: string): EnforcementResult {
  let result = reply;
  const violations: string[] = [];
  let shouldRetry = false;

  // ─── 1. IDENTITY ENFORCEMENT ────────────────────────
  // Zelrex must NEVER break character. If Claude says it's an AI assistant,
  // a language model, or refers to itself as anything other than Zelrex, fix it.
  const identityBreaks = [
    /\b(?:as an AI|I'm an AI|I am an AI|as a language model|as a large language model|I'm a language model)\b/gi,
    /\b(?:I'm Claude|I am Claude|my name is Claude|I'm made by Anthropic|I was (?:created|made|built) by Anthropic)\b/gi,
    /\b(?:as an assistant|I'm just an? (?:AI|assistant|chatbot|bot))\b/gi,
    /\b(?:I don't have (?:feelings|emotions|consciousness|personal experience))\b/gi,
    /\b(?:I'm a (?:chatbot|virtual assistant|digital assistant))\b/gi,
    /\b(?:powered by (?:Claude|GPT|OpenAI|Anthropic's technology))\b/gi,
  ];

  for (const pattern of identityBreaks) {
    if (pattern.test(result)) {
      violations.push("IDENTITY_BREAK: Claude broke character as Zelrex");
      // Replace common identity breaks with Zelrex-appropriate language
      result = result.replace(/\bAs an AI\b/gi, "As Zelrex");
      result = result.replace(/\bI'm an AI\b/gi, "I'm Zelrex");
      result = result.replace(/\bI am an AI\b/gi, "I am Zelrex");
      result = result.replace(/\bAs a language model\b/gi, "As a business engine");
      result = result.replace(/\bAs a large language model\b/gi, "As a business engine");
      result = result.replace(/\bI'm Claude\b/gi, "I'm Zelrex");
      result = result.replace(/\bI am Claude\b/gi, "I am Zelrex");
      result = result.replace(/\bI'm just an? AI\b/gi, "I'm Zelrex");
      result = result.replace(/\bI'm just an? assistant\b/gi, "I'm Zelrex");
      result = result.replace(/\bI'm just an? chatbot\b/gi, "I'm Zelrex");
      result = result.replace(/\bmy name is Claude\b/gi, "I'm Zelrex");
      result = result.replace(/\bI'm made by Anthropic\b/gi, "I'm Zelrex");
      result = result.replace(/\bI'm a chatbot\b/gi, "I'm Zelrex");
      result = result.replace(/\bI'm a virtual assistant\b/gi, "I'm Zelrex");
      result = result.replace(/\bI'm a digital assistant\b/gi, "I'm Zelrex");
      result = result.replace(/\bpowered by Claude\b/gi, "built by Anthropic");
      result = result.replace(/\bpowered by OpenAI\b/gi, "");
      result = result.replace(/\bpowered by GPT\b/gi, "");
      // If the identity break is severe (multiple occurrences), flag for retry
      const breakCount = (reply.match(/\b(?:AI assistant|language model|Claude|chatbot)\b/gi) || []).length;
      if (breakCount >= 3) shouldRetry = true;
    }
  }

  // ─── 2. FORBIDDEN ADVICE ENFORCEMENT ────────────────
  // These are things Zelrex must NEVER do, regardless of what the prompt says.
  // If Claude gives specific financial/legal/tax advice, catch it.
  const forbiddenAdvice = [
    { pattern: /\b(?:you should|I recommend|I advise)\s+(?:invest(?:ing)?|put(?:ting)? (?:money|funds|savings) (?:in|into))\b/i, type: "FINANCIAL_ADVICE", fix: "*Note: This is a general observation, not investment advice. Consult a licensed financial advisor.*" },
    { pattern: /\b(?:you should|I recommend|you need to)\s+(?:form|register|incorporate|file for)\s+(?:an? )?\s*(?:LLC|corporation|S.?corp|C.?corp|sole proprietorship)\b/i, type: "LEGAL_ADVICE", fix: "*Note: Business structure is a legal decision. Consult a business attorney or CPA for advice specific to your situation.*" },
    { pattern: /\b(?:you (?:can|should)|I recommend)\s+(?:deduct|write.?off|claim)\s+(?:your|the|this)\b/i, type: "TAX_ADVICE", fix: "*Note: Tax deductions depend on your specific situation. Consult a qualified tax professional.*" },
    { pattern: /\b(?:quit|leave|resign from) your (?:job|position|employer|current role) (?:now|immediately|today|this week|right away)\b/i, type: "RECKLESS_ADVICE", fix: "*Note: Major career transitions should be planned carefully. Consider building income on the side before leaving your current position.*" },
  ];

  for (const check of forbiddenAdvice) {
    if (check.pattern.test(result)) {
      violations.push(`${check.type}: Claude gave specific advice it shouldn't`);
      // Don't remove the content — just append the appropriate disclaimer
      if (!result.includes(check.fix)) {
        result += `\n\n${check.fix}`;
      }
    }
  }

  // ─── 3. GUARANTEE ENFORCEMENT ───────────────────────
  // Hard-block guarantee language. These get rewritten, not just disclaimed.
  const guaranteePatterns = [
    { pattern: /\byou will (?:definitely|certainly|absolutely|100%) (?:earn|make|get|have|succeed)\b/gi, replacement: "based on the data, you could potentially" },
    { pattern: /\bI (?:guarantee|promise) (?:you|that)\b/gi, replacement: "based on patterns I've seen," },
    { pattern: /\b(?:guaranteed|certain|inevitable) (?:success|results?|income|revenue|outcome)\b/gi, replacement: "possible results based on similar cases" },
  ];

  for (const g of guaranteePatterns) {
    if (g.pattern.test(result)) {
      violations.push("GUARANTEE: Claude used guarantee language");
      result = result.replace(g.pattern, g.replacement);
    }
  }

  // ─── 4. OFF-TOPIC ENFORCEMENT ───────────────────────
  // Zelrex only does freelance business. If Claude starts helping with
  // homework, recipes, code debugging, or general knowledge, catch it.
  const offTopicPatterns = [
    /\b(?:here'?s? (?:a|the) recipe|ingredients?:.*(?:cup|tablespoon|teaspoon))\b/i,
    /\b(?:def |function |class |import |const |let |var |console\.log|System\.out)\b/,
    /\b(?:the (?:answer|solution) (?:is|to your (?:homework|assignment|problem)))\b/i,
  ];

  for (const pattern of offTopicPatterns) {
    if (pattern.test(result) && result.length > 200) {
      // Only flag if the response is substantially off-topic (not just mentioning code briefly)
      const offTopicScore = offTopicPatterns.filter(p => p.test(result)).length;
      if (offTopicScore >= 2) {
        violations.push("OFF_TOPIC: Response appears to be outside Zelrex's scope");
        shouldRetry = true;
      }
    }
  }

  // ─── 5. DATA LEAKAGE ENFORCEMENT ────────────────────
  // Zelrex should never expose raw user data from the intelligence system.
  const dataLeakPatterns = [
    /\bone of (?:our|my) users?\s+(?:named|called|who)\b/i,
    /\buser (?:ID|#|number)\s*(?::|is)\s*\w+/i,
    /\b(?:their|this user's) (?:email|phone|address|name) (?:is|was)\b/i,
  ];

  for (const pattern of dataLeakPatterns) {
    if (pattern.test(result)) {
      violations.push("DATA_LEAK: Response may contain user-identifying information");
      result = result.replace(pattern, "[redacted for privacy]");
    }
  }

  // ─── 6. TAG STRIPPING ──────────────────────────────
  // Remove any bracketed tags that Claude might still use out of habit.
  // These should be expressed naturally, not as labels.
  const tagPatterns = /\s*\[(?:SEARCHED|ESTIMATED|PATTERN|VERIFIED|UNVERIFIED)\]\s*/gi;
  if (tagPatterns.test(result)) {
    violations.push("TAG_USAGE: Claude used bracketed tags instead of natural language");
    result = result.replace(tagPatterns, " ");
  }

  // ─── 7. DIRECTIVE LANGUAGE SOFTENING ───────────────
  // Catch cases where Claude gives directives instead of suggestions
  // on major decisions. Only applies to longer responses (real advice,
  // not casual chat).
  if (result.length > 300) {
    const directivePatterns = [
      { pattern: /\bYou need to\b/gi, replacement: "I'd strongly suggest" },
      { pattern: /\bYou must\b/gi, replacement: "I'd recommend" },
      { pattern: /\bYou have to\b/gi, replacement: "The strongest move here is to" },
    ];
    for (const d of directivePatterns) {
      if (d.pattern.test(result)) {
        violations.push("DIRECTIVE_LANGUAGE: Claude used directive instead of advisory language");
        result = result.replace(d.pattern, d.replacement);
      }
    }
  }

  // ─── 8. FAKE SOURCE DETECTION ───────────────────────
  // Catch patterns like "According to Mordor Intelligence" or "A 2024 study by..."
  // when no web search was performed. These are classic hallucination patterns.
  // We can't verify if a source is real, but we can soften overconfident attribution.
  const fakeSourcePatterns = [
    // "According to [Proper Noun Source]" without quotes or linking
    /\bAccording to ([A-Z][a-z]+(?:\s+[A-Z][a-z]+){0,3})\s*,/g,
    // "A [year] study by [Source]"
    /\bA (?:20\d{2}|recent) (?:study|report|analysis|survey) (?:by|from) ([A-Z][a-z]+(?:\s+[A-Z][a-z]+){0,2})/g,
    // "[Source] reports/found that"
    /\b([A-Z][a-z]+(?:\s+[A-Z][a-z]+){0,2}) (?:reports?|found|indicates?) that/g,
  ];
  
  // Only flag — don't auto-fix, since real sources also match these patterns.
  // Instead, check if ANY natural uncertainty language exists in the response.
  const hasNaturalUncertainty = /I(?:'d| would) estimate|from what I've seen|in my experience|I'm estimating|my best estimate|based on what I know|typically report|tends? to be|roughly|I'm not certain|I haven't verified|I don't have (?:solid|verified|specific) data|rough (?:number|estimate)|ballpark|worth verifying|worth checking|could vary/i.test(result);
  
  let hasSourceCitation = false;
  for (const pattern of fakeSourcePatterns) {
    if (pattern.test(result)) {
      hasSourceCitation = true;
      break;
    }
  }
  
  // If Claude cites a source but shows NO uncertainty anywhere, append a soft verification prompt.
  // Real web-searched content will still pass because it will include natural source references 
  // with quotes, URLs, or obvious sourcing context that comes from the search tool.
  if (hasSourceCitation && !hasNaturalUncertainty && result.length > 400) {
    const hasSearchEvidence = /\bsearched?\b|\bweb search\b|I found\b|I looked up\b|from (?:their|the) (?:website|site)|at https?:\/\//i.test(result);
    if (!hasSearchEvidence) {
      violations.push("UNVERIFIED_SOURCE: Response cites sources but shows no uncertainty or search evidence");
      result += "\n\n*One thing to flag: I referenced some sources above. If I didn't search the web in this conversation, take those citations as my best recollection — worth verifying before you rely on the specific numbers.*";
    }
  }

  // ─── 9. UNVERIFIED COMPETITOR PRICING ──────────────
  // Claude sometimes invents competitor pricing with specific dollar amounts.
  // Pattern: "[Company] charges/offers $X"
  const competitorPricingPattern = /\b([A-Z][a-z]+(?:\s*[A-Z][a-z]*)?)\s+(?:charges?|offers?|prices? (?:at|around))\s+\$[\d,]+/g;
  const competitorMatches = result.match(competitorPricingPattern);
  if (competitorMatches && competitorMatches.length > 0 && !hasNaturalUncertainty) {
    const hasSearchEvidence = /\bsearched?\b|I found\b|their (?:website|pricing page)|at https?:\/\//i.test(result);
    if (!hasSearchEvidence) {
      violations.push("UNVERIFIED_COMPETITOR_PRICE: Specific competitor pricing without search evidence");
      result += "\n\n*Note: the specific competitor pricing I mentioned is from my general knowledge, not a live check. Pricing changes — if you're making decisions based on it, worth a quick verification on their site.*";
    }
  }

  // ─── 10. FALSE MEMORY PROTECTION ───────────────────
  // Catch "as we discussed" / "you mentioned" in contexts where there's no proof
  // the user actually said that. We can't fully verify, but we can soften.
  const falseMemoryPatterns = [
    /\bas (?:we|you) (?:discussed|mentioned|talked about) earlier\b/i,
    /\bfrom what you (?:told|said|mentioned to) me (?:earlier|before)\b/i,
    /\bif I recall correctly from our (?:previous|past|earlier) (?:conversation|chat)\b/i,
  ];
  let memoryClaimFound = false;
  for (const pattern of falseMemoryPatterns) {
    if (pattern.test(result)) {
      memoryClaimFound = true;
      break;
    }
  }
  // Only flag memory claims if the response is from a fresh context (short input)
  // The chat route can set a flag to enable strict memory checking when userContext is empty
  // For now, if a memory claim is made, add a subtle hedge
  if (memoryClaimFound && result.length > 300) {
    // Log for awareness — don't auto-modify since memory claims can be legitimate
    violations.push("MEMORY_CLAIM: Response references prior conversation — verify memory context exists");
  }

  return {
    reply: result,
    violations,
    wasModified: result !== reply,
    shouldRetry,
  };
}


// ─── Full pipeline: validate + enforce + fact-check ──────────

export async function fullReliabilityPipeline(
  reply: string,
  userMessage: string,
  options: {
    checkFinancial?: boolean;
    checkContract?: boolean;
    checkGuarantee?: boolean;
    checkCompetitor?: boolean;
  } = {}
): Promise<{ reply: string; violations: string[] }> {
  // Step 1: Regex-based output validation
  let result = validateOutput(reply, options);

  // Step 2: Code-level enforcement (identity, forbidden advice, guarantees, data leaks)
  const enforcement = enforceRules(result);
  result = enforcement.reply;

  if (enforcement.violations.length > 0) {
    console.warn("[ZELREX ENFORCEMENT] Violations caught:", enforcement.violations);
  }

  // Step 3: Haiku fact-checker (async, catches hallucinations)
  result = await factCheck(result, userMessage);

  return {
    reply: result,
    violations: enforcement.violations,
  };
}